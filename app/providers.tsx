"use client";

import { ReactNode } from "react";
import { ClerkProvider, useAuth } from "@clerk/nextjs";
import { ConvexReactClient } from "convex/react";
import { ConvexProviderWithClerk } from "convex/react-clerk";

// Stubs are tolerated: until WAITING_ON_HUMAN.md keys are filled in,
// NEXT_PUBLIC_CONVEX_URL may be empty. We avoid constructing a client with an
// invalid URL (which throws) and degrade to rendering children unwrapped so
// the public scaffold still builds and renders.
const convexUrl = process.env.NEXT_PUBLIC_CONVEX_URL;
const convex = convexUrl ? new ConvexReactClient(convexUrl) : null;

export function Providers({ children }: { children: ReactNode }) {
  if (!convex) {
    // No Convex deployment configured yet — see WAITING_ON_HUMAN.md.
    return <ClerkProvider>{children}</ClerkProvider>;
  }

  return (
    <ClerkProvider>
      <ConvexProviderWithClerk client={convex} useAuth={useAuth}>
        {children}
      </ConvexProviderWithClerk>
    </ClerkProvider>
  );
}
